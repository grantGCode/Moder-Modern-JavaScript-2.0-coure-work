
let x = Math.random() * 100 + 1;

let y = Math.random() * 50 + 1.;

console.log(`${Math.floor(x)} + ${Math.floor(y)} = ${Math.floor(x+y)}`);
console.log(`${Math.floor(x)} - ${Math.floor(y)} = ${Math.floor(x-y)}`);
console.log(`${Math.floor(x)} * ${Math.floor(y)} = ${Math.floor(x*y)}`);
console.log(`${Math.floor(x)} / ${Math.floor(y)} = ${Math.floor(x/y)}`);
console.log(`${Math.floor(x)} % ${Math.floor(y)} = ${Math.floor(x%y)}`);